import java.util.HashSet;
public class hash_set {
    public static void main(String[] args) {
        HashSet<String> i=new HashSet<>();
        i.add("Utsav");
        i.add("17");
        i.add("Branch");
        i.add("Computer");
        i.add("Enrollment");
        i.add("216210307052");
        System.out.println(i);
        System.out.println(i.size());    //To Determine The Size Of The Hash Set
    }
}
